/**
 * Chaining "if" and "if else" statements
 */
let color = "green";

if ( color === "green" ) {
  console.log( "It is green color" );
} else if ( color === "yellow" ) {
  console.log( "It is yellow color" );
} else if ( color === "red" ) {
  console.log( "It is red color" );
} else {
  console.log( "Color is unknown" );
}








